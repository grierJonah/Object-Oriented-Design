package problem2;

/**
 * A class representing a gift card, CardNumber */
public class CardNumber {
  private Integer cardNumber;

  /**
   * Constructs a new gift card number
   * @param cardNumber - gift card number, represented as an Integer
   */
  public CardNumber(Integer cardNumber) {
    this.cardNumber = cardNumber;
  }

  /**
   * Returns gift card number
   * @return - returns gift card number, represented as an Integer
   */
  public Integer getCardNumber() {
    return this.cardNumber;
  }

}
